#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	char			*buff;

	if (s == NULL || f == NULL)
		return (NULL);
	i = 0;
	buff = (char *)malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (!buff)
		return (NULL);
	while (s[i])
	{
		buff[i] = f(i, s[i]);
		i++;
	}
	buff[i] = '\0';
	return (buff);
}
